@RunWith(SpringJUnit4ClassRunner.class)
@ContextConfiguration(locations = {"classpath:ddrs-test.xml", "classpath:ddrs-servlet.xml"})
@WebAppConfiguration
public class IndexControllerTest {

    private MockMvc mockMvc;

    @Autowired
    private WebApplicationContext webApplicationContext;

    @Before
    public void setUp() {
        mockMvc = MockMvcBuilders.webAppContextSetup(webApplicationContext).build();
    }

    @Test
    public void showSlash() throws Exception {
        mockMvc.perform(get("/"))
                .andExpect(status().isOk())
                .andExpect(view().name("index"))
                .andExpect(content().string(containsString("DDRS")));
    }

    @Test
    public void showIndex() throws Exception {
        mockMvc.perform(get("/index"))
                .andExpect(status().isOk())
                .andExpect(view().name("index"))
                .andExpect(content().string(containsString("DDRS")));
    }
}
